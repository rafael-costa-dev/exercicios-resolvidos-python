# Faça um programa que calcule todos os operadores artimeticos a partir de 2 numeros inseridos

# Entrada de dados
a = 5;
b = 2;

# Processamento de dados
soma = a + b;
subtracao = a - b;
multiplicacao = a * b;
divisao = a / b;
modulo = a % b;
exponenciacao = a ** b;

# Saída de dados
print('Soma é: ',soma);
print('Subtracao é: ',subtracao);
print('Multiplicação é: ',multiplicacao);
print('Divisão é: ',divisao);
print('Modulo é: ',modulo);
print('Exponenciação é: ',exponenciacao);

